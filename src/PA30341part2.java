public class PA30341part2 {
        public static void main(String[] args) {
            // Declare an integer variable x and assign 7 to it
            int x = 7;

            // Write an if-else statement to print "Less than 10" or "Greater than 10"
            if (x < 10) {
                System.out.println("Less than 10");
            } else {
                System.out.println("Greater than 10");
            }

            // Change x to equal 15
            x = 15;

            // Notice the result
            // Since x is now 15, the condition x < 10 is false, so it will print "Greater than 10"
        }
    }



